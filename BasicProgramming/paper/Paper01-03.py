'''
<단순 변수 사용>
    1. 순차형
    1-3. 현금교환기 (10000원, 1000원, 100원, 10원)
    1626035 이주호
'''

#계산할 가격 입력
money = int(input("교환할 금액을 정수로 입력 >> "))

##계산

#10000won
_10000won = money / 10000

#1000won
_1000won = money % 10000
_1000won = _1000won / 1000

#100won
_100won = money % 1000
_100won = _100won / 100

#10won
_10won = money % 100
_10won = _10won / 10

##계산 끝

print("%d원은 %d만 %d천 %d백 %d십원 입니다." % (money, _10000won, _1000won, _100won, _10won))


